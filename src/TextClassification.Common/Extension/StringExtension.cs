        public static string Clean(this string text)
        {
            text = text.Replace("`", "'");
            text = new string(text.Where(c => !char.IsControl(c)).ToArray());
            text = CharsToRemove.Aggregate(text, (current, c) => current.Replace(c, " "));
            var re = new Regex(@" [a-zA-Z] ", RegexOptions.Multiline);
            var mc = re.Matches(text);

            text = mc.Cast<Match>().Aggregate(text, (current, match) => current.Replace(match.Value, " "));

            text = text.TrimSpaces();
            return text;
        }

        public static IEnumerable<string> Prepare(this IEnumerable<string> list)
        {
            var result = new List<string>();

            foreach (var word in list)
            {
                var w = word.ToLower().Replace(" ", "");
                var isStopWord = false;
                foreach (var stopword in StopWords)
                {
                    if (w.Equals(stopword))
                        isStopWord = true;
                }
                if (!isStopWord)
                    result.Add(w);
            }
            //foreach (var word in list)
            //{
            //    result.Add(word.ToLower());
            //}
            return result;
        }

        /// <summary>
        /// Left 
        /// </summary>
        /// <param name="text"></param>
        /// <param name="length"></param>
        /// <returns></returns>
        public static string Left(this string text, int length = 300)
        {
            return text.Substring(0, text.Length < length ? text.Length : length);
        }

